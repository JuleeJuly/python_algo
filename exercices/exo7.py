age = int(input("Saisir l'age de votre enfant : "))
if age < 3 :
    print("age incorrect")
elif  3 <= age  <= 6:
    print("baby")
elif 7 <= age  <= 8 :
    print("poussin")
elif 9 <= age  <= 10 :
     print("pupille")
elif 11 <= age  <= 12 :
     print("minime")
elif  age  >= 13 :
     print("cadet")